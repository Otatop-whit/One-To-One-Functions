
public class Coordinate {
	private int x;
	private int y;
	public Coordinate(int x, int y) {
		this.x = x;
		this.y= y;	
	}
	public void setX(int number) {
		this.x = number;
	}	
	public int getX() {
		return x;
	}
	public void setY(int number) {
		this.y = number;
	}
	public int getY() {
		return y;
	}
	

}
